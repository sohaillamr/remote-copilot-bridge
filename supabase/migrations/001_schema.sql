-- ──────────────────────────────────────────────────────────────
-- 9. AUTO-CREATE PROFILE ON SIGNUP (trigger)
-- ──────────────────────────────────────────────────────────────

CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = ''
AS $$
BEGIN
    INSERT INTO public.profiles (id, email, display_name, avatar_url, subscription_status, trial_ends_at)
    VALUES (
        NEW.id,
        COALESCE(NEW.email, ''),
        COALESCE(NEW.raw_user_meta_data->>'full_name', NEW.raw_user_meta_data->>'name', ''),
        COALESCE(NEW.raw_user_meta_data->>'avatar_url', ''),
        'trial',
        now() + interval '7 days'   -- 7-day free trial
    );

    -- Also create a default 'user' role
    INSERT INTO public.user_roles (user_id, role)
    VALUES (NEW.id, 'user');

    RETURN NEW;
END;
$$;

CREATE OR REPLACE TRIGGER on_auth_user_created
    AFTER INSERT ON auth.users
    FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();


-- ──────────────────────────────────────────────────────────────
-- 10. RLS POLICIES
-- ──────────────────────────────────────────────────────────────

-- Helper: check if current user is admin (cached per statement)
CREATE OR REPLACE FUNCTION public.is_admin()
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = ''
AS $$
    SELECT COALESCE((SELECT auth.jwt()->>'user_role') = 'admin', false);
$$;

-- ── Profiles ──

CREATE POLICY "users_read_own_profile"
ON public.profiles FOR SELECT TO authenticated
USING (id = (SELECT auth.uid()) OR (SELECT public.is_admin()));

CREATE POLICY "users_update_own_profile"
ON public.profiles FOR UPDATE TO authenticated
USING (id = (SELECT auth.uid()))
WITH CHECK (id = (SELECT auth.uid()));

CREATE POLICY "admin_update_any_profile"
ON public.profiles FOR UPDATE TO authenticated
USING ((SELECT public.is_admin()))
WITH CHECK ((SELECT public.is_admin()));

-- ── Agents ──

CREATE POLICY "users_manage_own_agents"
ON public.agents FOR ALL TO authenticated
USING (user_id = (SELECT auth.uid()) OR (SELECT public.is_admin()));

-- ── Conversations ──

CREATE POLICY "users_manage_own_conversations"
ON public.conversations FOR ALL TO authenticated
USING (user_id = (SELECT auth.uid()) OR (SELECT public.is_admin()));

-- ── Messages (access via conversation ownership) ──

CREATE POLICY "users_read_own_messages"
ON public.messages FOR SELECT TO authenticated
USING (
    conversation_id IN (
        SELECT id FROM public.conversations WHERE user_id = (SELECT auth.uid())
    )
    OR (SELECT public.is_admin())
);

CREATE POLICY "users_insert_own_messages"
ON public.messages FOR INSERT TO authenticated
WITH CHECK (
    conversation_id IN (
        SELECT id FROM public.conversations WHERE user_id = (SELECT auth.uid())
    )
);

-- ── Prompt Logs ──

CREATE POLICY "admin_read_prompt_logs"
ON public.prompt_logs FOR SELECT TO authenticated
USING ((SELECT public.is_admin()));

CREATE POLICY "users_insert_prompt_logs"
ON public.prompt_logs FOR INSERT TO authenticated
WITH CHECK (user_id = (SELECT auth.uid()));


-- ──────────────────────────────────────────────────────────────
-- 11. REALTIME RLS (channel-level authorization)
-- ──────────────────────────────────────────────────────────────
-- These restrict Broadcast so users can only access their own channel.
-- Channel naming convention: "agent:{user_uuid}"
--
-- IMPORTANT: Also disable "Allow public access" in:
-- Dashboard → Project Settings → Realtime → uncheck "Allow public access"
-- ──────────────────────────────────────────────────────────────

CREATE POLICY "user_broadcast_select"
ON "realtime"."messages"
FOR SELECT TO authenticated
USING (
    realtime.topic() = 'agent:' || (SELECT auth.uid()::text)
    AND realtime.messages.extension = 'broadcast'
);

CREATE POLICY "user_broadcast_insert"
ON "realtime"."messages"
FOR INSERT TO authenticated
WITH CHECK (
    realtime.topic() = 'agent:' || (SELECT auth.uid()::text)
    AND realtime.messages.extension = 'broadcast'
);
