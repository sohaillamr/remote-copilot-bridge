CREATE EXTENSION IF NOT EXISTS "pgcrypto";

CREATE TYPE public.app_role AS ENUM ('admin', 'user');
CREATE TYPE public.subscription_status AS ENUM (
    'free', 'trial', 'active', 'cancelled', 'past_due', 'expired'
);


-- ──────────────────────────────────────────────────────────────
-- 1. USER ROLES (for admin JWT claim injection)
-- ──────────────────────────────────────────────────────────────

CREATE TABLE public.user_roles (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id    UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    role       public.app_role NOT NULL DEFAULT 'user',
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (user_id, role)
);

ALTER TABLE public.user_roles ENABLE ROW LEVEL SECURITY;

-- No public access — only the auth hook can read this
REVOKE ALL ON TABLE public.user_roles FROM authenticated, anon, public;


-- ──────────────────────────────────────────────────────────────
-- 2. CUSTOM ACCESS TOKEN HOOK (injects role into JWT)
-- ──────────────────────────────────────────────────────────────

CREATE OR REPLACE FUNCTION public.custom_access_token_hook(event jsonb)
RETURNS jsonb
LANGUAGE plpgsql
STABLE
AS $$
DECLARE
    claims     jsonb;
    user_role  public.app_role;
BEGIN
    -- Get the user's role
    SELECT role INTO user_role
    FROM public.user_roles
    WHERE user_id = (event->>'user_id')::uuid
    LIMIT 1;

    claims := event->'claims';

    IF user_role IS NOT NULL THEN
        claims := jsonb_set(claims, '{user_role}', to_jsonb(user_role));
    ELSE
        claims := jsonb_set(claims, '{user_role}', '"user"');
    END IF;

    event := jsonb_set(event, '{claims}', claims);
    RETURN event;
END;
$$;

-- Grant the auth admin role access to run the hook
GRANT USAGE ON SCHEMA public TO supabase_auth_admin;
GRANT EXECUTE ON FUNCTION public.custom_access_token_hook TO supabase_auth_admin;
GRANT SELECT ON TABLE public.user_roles TO supabase_auth_admin;

-- NOTE: After running this, enable the hook in:
-- Dashboard → Authentication → Hooks → Custom Access Token → select this function


-- ──────────────────────────────────────────────────────────────
-- 3. PROFILES (extends auth.users)
-- ──────────────────────────────────────────────────────────────

